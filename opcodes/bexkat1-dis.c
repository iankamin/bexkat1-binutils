#include "sysdep.h"
#include <stdio.h>

#define STATIC_TABLE
#define DEFINE_TABLE

#include "opcode/bexkat1.h"
#include "dis-asm.h"

extern const bexkat1_opc_info_t bexkat1_opc_info[128];
static fprintf_ftype fpr;
static void *stream;

/* Prototypes for local functions.  */
int print_insn_bexkat1 (bfd_vma, struct disassemble_info *);

/* Disassemble one instruction at address 'memaddr'.  Returns the number
   of bytes used by that instruction.  */
int print_insn_bexkat1 (bfd_vma memaddr, struct disassemble_info* info) {
  int status;
  stream = info->stream;
  unsigned char opcode;
  fpr = info->fprintf_func; 

  if ((status = info->read_memory_func(memaddr, &opcode, 1, info)))
    goto fail;


  fpr(stream, "%s", bexkat1_opc_info[opcode].name);

  return 1;

fail:
  info->memory_error_func(status, memaddr, info);
  return -1;
}
